# Задание 2: Дана строка, состоящая из русских слов, набранных заглавными буквами
# и разделённых пробелами (одним или несколькими). Вывести строку, содержащую
# эти же слова, разделённые одним пробелом и расположенные в алфавитном порядке.

stroka = input("Введите строку (слова заглавными буквами через пробел): ")
slova = stroka.split()
slova.sort()
result = " ".join(slova)
print("Результат:", result)
