# Практическая работа 6
# Задание 3: Дан список A размера N и целое число K (1 < K < 4, K < N).
# Осуществить циклический сдвиг элементов списка влево на K позиций
# (при этом A[N] перейдёт в A[N-K], A[N-1] — в A[N-K-1], ..., A[1] — в A[N-K+1]).
# Допускается использовать вспомогательный список из 4 элементов.

n = int(input("Введите размер списка N: "))
A = []
for i in range(n):
    x = int(input(f"A[{i+1}]: "))
    A.append(x)

k = int(input("Введите K (1 < K < 4, K < N): "))

helper = A[:k]
for i in range(n - k):
    A[i] = A[i + k]
for i in range(k):
    A[n - k + i] = helper[i]

print("Список после сдвига:", A)
